//Use pre-decrement operator in while's boolean_expression to print HELLO 5 times on to the console.

import java.util.*;

public class Que1 {
	public static void main(String[] args) {
		int itration = 5; 
		while(--itration >= 0) { 
			System.out.println("HELLO");
		}
	}
}
